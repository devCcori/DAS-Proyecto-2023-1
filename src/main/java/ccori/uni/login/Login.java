    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUser.getText();
        String password = new String(txtPass.getPassword());
           
        Connection cn = null;
        
        try {
            // Inicio de TX
            cn = sqlConnection.getConnection();
            cn.setAutoCommit(false); // Inicio de TX

            String query = "SELECT COUNT(*) FROM Logins WHERE Username = ? AND Password = ?";

            PreparedStatement statement = cn.prepareStatement(query);
            statement.setString(1, username);
            statement.setString(2, password);

            ResultSet resultSet = statement.executeQuery();
            resultSet.next();
            int count = resultSet.getInt(1);

            if (count == 1) {                
                new MainMenu().setVisible(true);
                System.out.println("Inicio de sesi칩n exitoso");
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usuario y/o Contrase침a incorrectos", "Error", JOptionPane.ERROR_MESSAGE);                
            }

            resultSet.close();
            statement.close();
            cn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

